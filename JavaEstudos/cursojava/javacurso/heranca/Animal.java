package heranca;

public class Animal {
    private String locomove;
    private String alimente;

    public Animal(){

    }

    public String getLocomove(){
        return locomove;
    }

    public void setLocomove(String locomove){
        this.locomove = locomove;
    }

    public String getAlimenta(){
        return alimente;
    }

    public void setAlimenta(String alimente){
        this.alimente = alimente;
    }


}
